        $Exam->Prenom = $request->input('Prenom');
        $Exam->Code_Exam = $request->input('Code_Exam');
        $Exam->Matricule = $request->input('Matricule');
        $Exam->save();
        $Code =$request->input('Code_Exam');
        return view('Exams.AffichageExam',[
            'Code'=>$Code
        ]);
    }

